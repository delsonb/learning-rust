// Section: avoinding duplication among types with generics

fn largest_generic<T: std::cmp::PartialOrd>(list: &[T]) -> &T {
    let mut largest = &list[0];

    for item in list {
        if item > largest {  // T must be a type the implements ordering
            largest = item;
        }
    }

    largest
}

